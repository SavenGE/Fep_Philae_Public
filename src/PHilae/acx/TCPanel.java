    private void dedTierButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_dedTierButtonActionPerformed
    {//GEN-HEADEREND:event_dedTierButtonActionPerformed
        // TODO add your handling code here:
        boolean save = dedTierButton.getText().equals("Add");
        if (save ? getWorker().validateFields(chargesDialog, deductionTiersPanel) : true) {
            if (save) {
                AXTier tier = new AXTier();
                tier.setType("D");
                tier.setTierMax(getWorker().formatDecimal(new BigDecimal(dedTierMaxField.getText().trim())));

                tier.setValue(getWorker().formatDecimal(new BigDecimal(dedTierValueField.getText().trim())));
                tier.setTierMin(getTierFloor(getDeduction().getTiers().values(), tier.getTierMax()));
                getDeduction().getTiers().put(tier.getTierMax(), tier);

                if (getDeduction().getTiers().containsKey(tier.getTierMax())) {
                    JOptionPane.showMessageDialog(chargesDialog, "Tier saved successfully.");
                    getValue().getDeductions().put(getDeduction().getRecId(), getDeduction());
                    setDeductionTierTree();
                    getWorker().selectTreeNode(deductionTierTree, tier);
                }
            } else {
                DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) deductionTierTree.getLastSelectedPathComponent();
                if (selectedNode != null && selectedNode.getUserObject() instanceof AXTier) {
                    AXTier tier = (AXTier) selectedNode.getUserObject();
                    getDeduction().getTiers().remove(tier.getTierMax());
                    if (!getDeduction().getTiers().containsKey(tier.getTierMax())) {
                        JOptionPane.showMessageDialog(chargesDialog, "Tier removed successfully.");
                        getValue().getDeductions().put(getDeduction().getRecId(), getDeduction());
                        setDeductionTierTree();
                    }
                }
            }
        }
    }//GEN-LAST:event_dedTierButtonActionPerformed

