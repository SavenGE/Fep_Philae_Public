    private void chargeTypeBoxItemStateChanged(java.awt.event.ItemEvent evt)//GEN-FIRST:event_chargeTypeBoxItemStateChanged
    {//GEN-HEADEREND:event_chargeTypeBoxItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String chargeType = getWorker().getBoxValue(chargeTypeBox);
            minAmountField.setEditable("O".equals(chargeType) || "P".equals(chargeType));
            maxAmountField.setEditable("O".equals(chargeType) || "P".equals(chargeType));
            chargeValueField.setEditable(!"T".equals(chargeType));
            if (Objects.equals(chargeType, getValue().getType())) {
                minAmountField.setText(getValue().getMin().toPlainString());
                maxAmountField.setText(getValue().getMax().toPlainString());
                chargeValueField.setText(getValue().getValue().toPlainString());
            } else {
                chargeValueField.setText("");
                minAmountField.setText("0.00");
                maxAmountField.setText("0.00");
            }
            switch (chargeType) {
                case "O":
                    valueLabel.setText("Percentage");
                    minLabel.setText("Floor Amount");
                    break;
                case "P":
                    minLabel.setText("Min. Charge");
                    valueLabel.setText("Percentage");
                    break;
                case "T":
                    chargeValueField.setText("0.00");
                    minLabel.setText("Min. Charge");
                    valueLabel.setText("Charge Value");
                    break;
                default:
                    minLabel.setText("Min. Charge");
                    valueLabel.setText("Charge Amount");
                    break;
            }
            setTiersPanel(chargeTierTree, chargeTierPanel, Objects.equals(getWorker().getBoxValue(chargeTypeBox), "T"));
        }
    }//GEN-LAST:event_chargeTypeBoxItemStateChanged

