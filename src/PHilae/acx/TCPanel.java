    private void chargeValueFieldFocusLost(java.awt.event.FocusEvent evt)//GEN-FIRST:event_chargeValueFieldFocusLost
    {//GEN-HEADEREND:event_chargeValueFieldFocusLost
        // TODO add your handling code here:
        String val = chargeValueField.getText().trim();
        if (!getWorker().isBlank(val)) {
            BigDecimal amount = getWorker().convertToType(chargeValueField.getText().trim(), BigDecimal.class);
            if (!getWorker().validateAmount(amount, true)) {
                chargeValueField.requestFocus();
                chargeValueField.selectAll();
            }
            if (!getWorker().isNumber(val)) {
                chargeValueField.requestFocus();
                chargeValueField.selectAll();
            } else {
                val = getWorker().formatDecimal(new BigDecimal(val)).toPlainString();
                chargeValueField.setText(val);
                if (chargeTypeBox.getSelectedIndex() == 0) {
                    minAmountField.setText(val);
                    maxAmountField.setText(val);
                }
            }
        }
    }//GEN-LAST:event_chargeValueFieldFocusLost

