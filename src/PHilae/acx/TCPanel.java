    private void validateTierMax(JTree tierTree, HashMap<BigDecimal, AXTier> tiers, JTextField tierMaxField, JTextField tierMinField) {
        // TODO add your handling code here:
        String maxAmt = tierMaxField.getText().trim();
        if (!getWorker().isBlank(maxAmt)) {
            BigDecimal ceiling;
            if ((ceiling = getWorker().convertToType(maxAmt, BigDecimal.class)) != null) {
                getWorker().formatDecimalValue(tierMaxField);
                tierMinField.setText(getTierFloor(tiers.values(), ceiling).toPlainString());
                if (tiers.containsKey(new BigDecimal(tierMaxField.getText()))) {
                    getWorker().selectTreeNode(tierTree, tiers.get(new BigDecimal(tierMaxField.getText())));
                }
            } else {
                tierMaxField.requestFocus();
                tierMaxField.selectAll();
            }
        }
    }

    private void chargeTierButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_chargeTierButtonActionPerformed
    {//GEN-HEADEREND:event_chargeTierButtonActionPerformed
        // TODO add your handling code here:
        boolean save = chargeTierButton.getText().equals("Add");
        if (save ? getWorker().validateFields(chargesDialog, chargeTiersPanel) : true) {
            if (save) {
                AXTier tier = new AXTier();
                tier.setType("C");
                tier.setTierMax(getWorker().formatDecimal(new BigDecimal(chargeTierMaxField.getText().trim())));

                tier.setValue(getWorker().formatDecimal(new BigDecimal(chargeTierValueField.getText().trim())));
                tier.setTierMin(getTierFloor(getValue().getTiers().values(), tier.getTierMax()));
                getValue().getTiers().put(tier.getTierMax(), tier);

                if (getValue().getTiers().containsKey(tier.getTierMax())) {
                    JOptionPane.showMessageDialog(chargesDialog, "Tier saved successfully.");
                    getCharge().getValues().put(getValue().getCurrency(), getValue());
                    setChargeTierTree();
                    getWorker().selectTreeNode(chargeTierTree, tier);
                }
            } else {
                DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) chargeTierTree.getLastSelectedPathComponent();
                if (selectedNode != null && selectedNode.getUserObject() instanceof AXTier) {
                    AXTier tier = (AXTier) selectedNode.getUserObject();
                    getValue().getTiers().remove(tier.getTierMax());
                    if (!getValue().getTiers().containsKey(tier.getTierMax())) {
                        JOptionPane.showMessageDialog(chargesDialog, "Tier removed successfully.");
                        getCharge().getValues().put(getValue().getCurrency(), getValue());
                        setChargeTierTree();
                    }
                }
            }
        }
    }//GEN-LAST:event_chargeTierButtonActionPerformed

