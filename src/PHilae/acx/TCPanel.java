    public void setWaiverTree() {
        populateWaiverProductsBox(false);
        DefaultMutableTreeNode rootNode = ((DefaultMutableTreeNode) waiverTree.getModel().getRoot());

        rootNode.removeAllChildren();
        rootNode.add(new DefaultMutableTreeNode("Add Waiver"));

        HashMap<Long, TCWaiver> waiversMap = getCharge().getWaivers();
        for (TCWaiver waiver : getWorker().sortArray(waiversMap.values().toArray(new TCWaiver[0]), true)) {
            waiver.setDescription(waiver.getProductId() + "~[" + waiver.getWaivedPercentage().toPlainString() + "%]" + (Objects.equals(waiver.getProductId(), 0L) ? APController.getClient().queryProductName(waiver.getProductId()) : "All Products"));
            rootNode.add(new DefaultMutableTreeNode(waiver));
        }
        getWorker().expandAllNodes(waiverTree, TCWaiver.class);
    }

