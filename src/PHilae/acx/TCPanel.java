    public void setDeductionTierTree() {
        DefaultMutableTreeNode rootNode = ((DefaultMutableTreeNode) deductionTierTree.getModel().getRoot());
        rootNode.removeAllChildren();

        rootNode.add(new DefaultMutableTreeNode("Add Tier"));
        setTierFloors(getDeduction().getTiers());

        for (AXTier tier : getWorker().sortArray(getDeduction().getTiers().values().toArray(new AXTier[0]), true)) {
            rootNode.add(new DefaultMutableTreeNode(tier));
        }
        setTiersPanel(deductionTierTree, deductionTierPanel, Objects.equals(getWorker().getBoxValue(deductionTypeBox), "T"));
        getWorker().expandAllNodes(deductionTierTree, AXTier.class);
    }

    private void setTiersPanel(JTree tierTree, JPanel tierPanel, boolean enabled) {

        tierTree.setEnabled(enabled);
        for (Component c : tierPanel.getComponents()) {
            if (!(c instanceof JLabel)) {
                c.setEnabled(enabled);
                if (c instanceof JTextField) {
                    ((JTextField) c).setText("");
                }
            }
        }
    }

    private void displayValue(String currency) {
        if (!getWorker().isBlank(getValue().getCurrency())) {
            updateValue();
        }
        setValue(getCharge().getValues().containsKey(currency) ? getCharge().getValues().get(currency) : new TCValue());
        getWorker().selectBoxValue(chargeTypeBox, getValue().getType());
        chargeValueField.setText(getValue().getValue().toPlainString());
        minAmountField.setText(getValue().getMin().toPlainString());
        maxAmountField.setText(getValue().getMax().toPlainString());
        getValue().setCurrency(currency);
        setChargeTierTree();
        setDeductionTree();
    }

    private void updateValue() {
        try {
            getValue().setType(getWorker().getBoxValue(chargeTypeBox));
            getValue().setValue(getWorker().convertToType(chargeValueField.getText().trim(), BigDecimal.class));
            getValue().setMin(getWorker().convertToType(minAmountField.getText(), BigDecimal.class));
            getValue().setMax(getWorker().convertToType(maxAmountField.getText(), BigDecimal.class));
            getCharge().getValues().put(getValue().getCurrency(), getValue());
        } catch (Exception ex) {
            ex = null;
        }
    }

